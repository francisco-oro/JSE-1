// Task 3
let numbers = [21, 45, 100, 12, 11, 78, 61, 4, 39, 22];
// Writing out all the numbers in the console
for(number of numbers){
    console.log(number);
}
// Writing out only even numbers
for(number of numbers){
    if(number % 2 == 0){
        console.log(number);
    }
}
// Writing out only those larger than 10 and, at the same time, smaller than 60
for(number of numbers){
    if(number > 10 && number < 60){
        console.log(number)
    }
}
